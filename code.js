function sayHello(input) {
    if(input === "Alex"){
        return "Hello, Alex!";
    } else if(input === "Jane"){
        return "Hello, Jane!"
    } else if(input === "Pat") {
        return "Hello, Pat!"
    }
    else {
            return "";
        }
}

